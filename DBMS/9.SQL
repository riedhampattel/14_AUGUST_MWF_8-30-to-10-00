-- PRIME NUMBER
DECLARE
	X NUMBER := 11;
	I NUMBER := 2;
	CNT NUMBER := 0;
BEGIN
	WHILE I<X LOOP
		IF MOD(X,I) = 0 THEN
			CNT := CNT + 1;
		END IF;
		I := I+1;
	END LOOP;
