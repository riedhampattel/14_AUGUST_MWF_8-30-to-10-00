		DBMS_OUTPUT.PUT_LINE(X||' IS A PRIME NUMBER');
	ELSE
		DBMS_OUTPUT.PUT_LINE(X||' IS NOT A PRIME NUMBER');
	END IF;
END;

-- TRIANGLE PATTERN
DECLARE
	R NUMBER := 10;
	SPC NUMBER;
	I NUMBER;
	J NUMBER;
	K NUMBER;
BEGIN
    SPC := R-1;
	FOR I IN 1..R LOOP
		FOR J IN 1..SPC LOOP
			DBMS_OUTPUT.PUT(' ');
		END LOOP;
		FOR K IN 1..I LOOP
			DBMS_OUTPUT.PUT('* ');
		END LOOP;
		DBMS_OUTPUT.PUT_LINE('');
		SPC := SPC-1;
	END LOOP;
END;

-- EXPLICIT CURSOR
DECLARE
	EID EMPLOYEES.EMPLOYEE_ID%TYPE;
	FNAME EMPLOYEES.FIRST_NAME%TYPE;
	LNAME EMPLOYEES.LAST_NAME%TYPE;
	SAL EMPLOYEES.SALARY%TYPE;
	CURSOR EMP IS SELECT EMPLOYEE_ID,FIRST_NAME,LAST_NAME,SALARY FROM EMPLOYEES;
BEGIN
	OPEN EMP;
	LOOP
		FETCH EMP INTO EID,FNAME,LNAME,SAL;
		DBMS_OUTPUT.PUT_LINE('SALARY OF EMPLOYEE ID '||EID||' '||FNAME||' '||LNAME||' IS '||SAL);
		EXIT WHEN EMP%NOT FOUND;
	END LOOP;
	CLOSE EMP;
END;

